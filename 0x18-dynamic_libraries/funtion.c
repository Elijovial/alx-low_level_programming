#include <stdio.h>

/**
 * add - function that add two numbers
 * @a: first integer
 * @b: second integer
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - function that subtract two numbers
 * @a: first integer
 * @b: second integer
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - function that multiply two numbers
 * @a: first integer
 * @b: second integer
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - function that divide two numbers
 * @a: first integer
 * @b: second integer
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - function that gets the modulos of two numbers
 * @a: first integer
 * @b: second integer
 */

int mod(int a, int b)
{
	return (a % b);
}
